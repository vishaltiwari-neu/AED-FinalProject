    private void populateTable(List<Dictionary> apartmentList) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        for (Dictionary d : apartmentList) {
            Object[] row = new Object[3];
            row[0] = d.get("address");
            row[1] = d.get("owner");
            row[2] = d.get("finalApproval");
            model.addRow(row);
        }
    }

